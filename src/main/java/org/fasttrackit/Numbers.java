package org.fasttrackit;

public class Numbers {
    public static int addition(int x, int y){
        int z = x + y;
        System.out.println("Result of the addition is: "+z);
        return z;
    }
    public static int subtraction(int x, int y){
        int z = x - y;
        System.out.println("Result of the subtraction is: "+z);
        return z;
    }
    public static int multiplication(int x, int y){
        int z = x * y;
        System.out.println("Result of the multiplication is: "+z);
        return z;
    }
    public static double division(double x, double y){
        double z = x / y;
        System.out.println("Result of the division is: "+z);
        return z;
    }
    public static double modulo(int x, int y){
        int z = x - y;
        System.out.println("Result of the modulo is: "+z);
        return z;
    }
}
